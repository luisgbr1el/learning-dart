main() {
  var valor1 = 60;
  var valor2 = 10;
  print("Valor 1 = $valor1\nValor 2 = $valor2\n");
  
  // Soma
  var soma = valor1 + valor2;
  print("A soma entre eles é $soma");
  
  // Subtração
  var sub = valor1 - valor2;
  print("A subtração entre eles é $sub");
  
  // Multiplicação
  var mult = valor1 * valor2;
  print("A multiplicação entre eles é $mult");
  
  // Divisão
  var div = valor1 / valor2;
  print("A divisão entre eles é $div");
  
  // Resto da divisão
  var resto = valor1 % valor2;
  print("O resto da divisão entre eles é $resto");
}
